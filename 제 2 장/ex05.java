package chapter02;

public class ex05 {

	public static void main(String[] args) {
		int num1 = 5;
		System.out.println(++num1);
		System.out.println(++num1);
		System.out.println(num1);
		int num2 = 5;
		System.out.println(num2++);
		System.out.println(num2++);
		System.out.println(num2);
		
		int num3 = 5;
		System.out.println(--num3);
		System.out.println(--num3);
		System.out.println(num3);
		int num4 = 5;
		System.out.println(num4--);
		System.out.println(num4--);
		System.out.println(num4);


	}

}
